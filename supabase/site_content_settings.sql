-- Structured, administrator-managed site copy and contact details.
-- Public visitors can read values; only administrators can change them.

create table if not exists public.site_content (
  key text primary key,
  value text not null default '',
  label text not null,
  section text not null,
  sort_order integer not null default 0,
  is_multiline boolean not null default false,
  updated_at timestamptz not null default now(),
  constraint site_content_key_check check (
    key ~ '^[a-z][a-z0-9_]*(\.[a-z][a-z0-9_]*)+$'
  ),
  constraint site_content_value_length check (char_length(value) <= 5000)
);

create index if not exists site_content_section_sort_idx
  on public.site_content (section, sort_order, key);

alter table public.site_content enable row level security;

grant select on public.site_content to anon, authenticated;
grant insert, update on public.site_content to authenticated;

drop policy if exists "site_content_public_read" on public.site_content;
create policy "site_content_public_read"
on public.site_content
for select
to anon, authenticated
using (true);

drop policy if exists "site_content_admin_write" on public.site_content;
create policy "site_content_admin_write"
on public.site_content
for all
to authenticated
using (public.is_admin(auth.uid()))
with check (public.is_admin(auth.uid()));

